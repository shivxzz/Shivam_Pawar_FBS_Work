#1. & (AND) : Consider 1 if both bits are 1 otherwise 0
print(13 & 6)

#2. | (OR) : Consider 0 if both bits are 0 otherwise 1
print(13 | 6)

#3. ^ (XOR) : Consider 1 for different bits, 0 for same
print(13 ^ 6)

#4. ~ (NOT): ~x = - (x + 2)
print(~10)

#5: << (Left Shift): 
print(13 << 2)

#6: >> (Right Shift):
print(13 >> 2)